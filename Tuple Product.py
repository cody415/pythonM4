# Tuples
tup1 = (4, 3, 2, 2, -1, 18)
tup2 = (2, 4, 8, 8, 3, 2, 9)

# Calculate product for tup1
product1 = 1
for num in tup1:
    product1 *= num

# Calculate product for tup2
product2 = 1
for num in tup2:
    product2 *= num

# Print results
print("Tuple 1:", tup1)
print("Product of Tuple 1:", product1)

print("Tuple 2:", tup2)
print("Product of Tuple 2:", product2)
